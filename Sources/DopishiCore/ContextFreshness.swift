import Foundation

/// Свежесть AX-префикса относительно клавиатурного буфера.
///
/// В Electron (Claude) AX-дерево обновляется с задержкой 100-400мс, а события клавиш приходят
/// мгновенно (CGEventTap). Если debounce истекает раньше, чем AX догонит, движок генерит по
/// УСТАРЕВШЕМУ префиксу и достраивает старое состояние текста ("давай" -> "пойдём на велосипед",
/// хотя пользователь уже напечатал "давай про"). KeystrokeBuffer (typedSinceFocus) знает
/// набранное немедленно - сверяемся с ним.
public enum ContextFreshness {
    /// Свеж ли AX-префикс. Если буфер пуст (вставка/после клика/сброс) - доверяем AX.
    /// Иначе свежий префикс ОБЯЗАН оканчиваться на недавно набранное: текст до каретки -
    /// это (возможно префикс из поля) + набранный хвост. Если не оканчивается - AX отстал.
    public static func isFresh(_ ctx: EditingContext) -> Bool {
        ctx.typedSinceFocus.isEmpty || ctx.precedingText.hasSuffix(ctx.typedSinceFocus)
    }
}
